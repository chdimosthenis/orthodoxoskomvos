def write_entry(e: dict, *, force: bool) -> str:
    target = FATHERS_DIR / f"{e['slug']}.md"
    if target.exists() and not force:
        return "skip"

    fm_lines = [
        "---",
        f"name: \"{e['name']}\"",
        f"fullName: \"{e['fullName']}\"",
        f"century: {e['century']}",
    ]
    if "feastDay" in e:
        fm_lines.append(f"feastDay: \"{e['feastDay']}\"")
    fm_lines.extend([
        f"summary: \"{e['summary']}\"",
        "language: el",
        "license: original",
        "draft: false",
        "---",
        "",
        e["body"].strip(),
        "",
    ])
    target.write_text("\n".join(fm_lines), encoding="utf-8")
    return "wrote"


def main() -> int:
    p = argparse.ArgumentParser()
    p.add_argument("--force", action="store_true")
    args = p.parse_args()

    if not FATHERS_DIR.is_dir():
        print(f"ERROR: {FATHERS_DIR} not found", file=sys.stderr)
        return 1

    # Sanity-check: ensure we never overwrite the existing Greek entry.
    slugs = [e["slug"] for e in ENTRIES]
    if EXISTING_GREEK_SLUG in slugs:
        print(f"ERROR: ENTRIES contains protected slug '{EXISTING_GREEK_SLUG}'", file=sys.stderr)
        return 1

    if len(ENTRIES) != 30:
        print(f"WARNING: expected 30 ENTRIES, got {len(ENTRIES)}", file=sys.stderr)

    wrote = skipped = 0
    for e in ENTRIES:
        result = write_entry(e, force=args.force)
        if result == "wrote":
            wrote += 1
            print(f"+ {e['slug']:30} ({e['century']}c)")
        else:
            skipped += 1
            print(f"= {e['slug']:30} (exists)")
    print(f"\nCreated {wrote}/30 entries")
    if skipped:
        print(f"Skipped {skipped} existing (use --force to overwrite)")
    return 0


if __name__ == "__main__":
    sys.exit(main())
