def write_entry(e: dict, pub_date: str, *, force: bool) -> str:
    target = ARTICLES_DIR / f"{e['slug']}.md"
    if target.exists() and not force:
        return "skip"

    fm_lines = [
        "---",
        f"title: \"{e['title']}\"",
        f"description: \"{e['description']}\"",
        f"pubDate: {pub_date}",
        "author: \"Σύνταξη\"",
        "language: el",
        "license: original",
        "tags: " + str(list(e["tags"])).replace("'", '"'),
        "---",
        "",
        e["body"].strip(),
        "",
    ]
    target.write_text("\n".join(fm_lines), encoding="utf-8")
    return "wrote"


