def seed_el(*, force: bool = False, dry_run: bool = False) -> None:
    log(f"Seeding {len(SAINTS_EL)} Greek saints...")
    written, skipped = 0, 0
    for entry in SAINTS_EL:
        slug = entry["slug"]
        if check_exists("saints", slug) and not force:
            log(f"  skip {slug} (exists)", level="warn")
            skipped += 1
            continue
        if dry_run:
            log(f"  would write saints/{slug}.md")
            continue
        write_content("saints", slug, entry["frontmatter"], entry["body"], force=force)
        log(f"  wrote saints/{slug}.md", level="ok")
        written += 1
    log(f"done — written: {written}, skipped: {skipped}", level="ok")


def main() -> None:
    p = argparse.ArgumentParser(description="Seed the saints calendar with curated entries.")
    p.add_argument("--force", action="store_true", help="Overwrite existing files")
    p.add_argument("--dry-run", action="store_true", help="Preview without writing")
    args = p.parse_args()

    seed_el(force=args.force, dry_run=args.dry_run)


if __name__ == "__main__":
    main()
