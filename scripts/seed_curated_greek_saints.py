def write_entry(e: dict, *, force: bool) -> str:
    target = SAINTS_DIR / f"{e['slug']}.md"
    if target.exists() and not force:
        return "skip"

    fm_lines = [
        "---",
        f"name: \"{e['name']}\"",
        f"feastDay: \"{e['feastDay']}\"",
        f"category: {e['category']}",
        f"life: \"{e['life']}\"",
        f"language: el",
        f"wikipediaTitle: \"{e['wikipediaTitle']}\"",
        "license: original",
        "---",
        "",
        e["body"],
        "",
    ]
    target.write_text("\n".join(fm_lines), encoding="utf-8")
    return "wrote"


def main() -> int:
    p = argparse.ArgumentParser()
    p.add_argument("--force", action="store_true", help="Overwrite existing entries")
    args = p.parse_args()

    if not SAINTS_DIR.is_dir():
        print(f"ERROR: {SAINTS_DIR} not found", file=sys.stderr)
        return 1

    wrote = skipped = 0
    for e in ENTRIES:
        result = write_entry(e, force=args.force)
        if result == "wrote":
            wrote += 1
            print(f"+ {e['slug']}")
        else:
            skipped += 1
            print(f"= {e['slug']} (exists)")
    print(f"\nTotal: {wrote} written, {skipped} skipped")
    print("Run fetch_icon.py --update-all to populate iconUrl from Wikipedia.")
    return 0


if __name__ == "__main__":
    sys.exit(main())
