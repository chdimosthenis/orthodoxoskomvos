FRONTMATTER_TEMPLATE = (
    "---\n"
    'title: "{title}"\n'
    'description: "{description}"\n'
    "pubDate: {pub_date}\n"
    'author: "Σύνταξη"\n'
    "language: el\n"
    "license: original\n"
    "tags: [{tags}]\n"
    "---\n"
    "\n"
    "{body}"
)


def render_tags(tags: list[str]) -> str:
    return ", ".join(f'"{t}"' for t in tags)


def write_entry(entry: dict[str, Any], pub_date: str, force: bool) -> bool:
    slug = entry["slug"]
    path = ARTICLES_DIR / f"{slug}.md"
    if path.exists() and not force:
        print(f"  skip (exists): {slug}.md")
        return False

    content = FRONTMATTER_TEMPLATE.format(
        title=entry["title"],
        description=entry["description"],
        pub_date=pub_date,
        tags=render_tags(entry["tags"]),
        body=entry["body"].rstrip() + "\n",
    )
    path.write_text(content, encoding="utf-8")
    action = "overwrite" if path.exists() and force else "create"
    print(f"  {action}: {slug}.md ({pub_date})")
    return True


