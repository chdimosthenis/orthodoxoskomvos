def write_entry(entry: dict[str, Any], pub_date: str, force: bool) -> bool:
    """Write a single entry to the articles directory. Return True if written."""
    path = ARTICLES_DIR / f"{entry['slug']}.md"
    if path.exists() and not force:
        return False
    tags_yaml = ", ".join(f'"{t}"' for t in entry["tags"])
    frontmatter = (
        "---\n"
        f'title: "{entry["title"]}"\n'
        f'description: "{entry["description"]}"\n'
        f"pubDate: {pub_date}\n"
        f'author: "Σύνταξη"\n'
        f"language: el\n"
        f"license: original\n"
        f"tags: [{tags_yaml}]\n"
        "---\n\n"
    )
    path.write_text(frontmatter + entry["body"], encoding="utf-8")
    return True


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--force", action="store_true",
                        help="Overwrite existing files.")
    args = parser.parse_args()

    if not ARTICLES_DIR.exists():
        print(f"ERROR: articles directory not found: {ARTICLES_DIR}",
              file=sys.stderr)
        return 1

    if len(ENTRIES) != len(PUB_DATES):
        print(f"ERROR: ENTRIES ({len(ENTRIES)}) != PUB_DATES "
              f"({len(PUB_DATES)})", file=sys.stderr)
        return 1

    written = 0
    skipped = 0
    for entry, pub_date in zip(ENTRIES, PUB_DATES):
        if write_entry(entry, pub_date, args.force):
            written += 1
        else:
            skipped += 1

    print(f"Created {written}/{len(ENTRIES)} entries"
          + (f" (skipped {skipped})" if skipped else ""))
    return 0


if __name__ == "__main__":
    sys.exit(main())
